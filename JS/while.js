var contador =0;
while (contador<10) {
    console.log("Vuelta de WHILE N. " + contador);
    contador++;
}
console.log("____________________________________");

//ESTRUTURA FOR
for (var i = 0; i < 10; i++) {
    console.log("Vuelta de FOR N. " + i); 
}
console.log("____________________________________");

//ESTRUCTURA DO WHILE
var contador = 0;

do {
    console.log("Vuelta de DO WHILE N. " + contador)
    contador++;
} while (contador<10);